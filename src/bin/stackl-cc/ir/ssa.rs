pub enum Opcode {
	Segment(String),
	Nop,
	Add,
	Sub,
	Mul,
	Div,
	Mod,
	Eq,
	Ne,
	Gt,
	Lt,
	Ge,
	Le,
	And,
	Or,
	Not,
	Swap,
	Dup,
	Halt,
	Pop,
	Ret,
	Retv,
	Neg,
	PushCVarInd,
	Outs,
	Inp,
	PushFp,
	JmpUser,
	Trap,
	Rti,
	Calli,
	PushReg,
	PopReg,
	BAnd,
	BOr,
	BXOr,
	ShiftLeft,
	ShiftRight,
	PushVarInd,
	PopCVarInd,
	PopVarInd,
	Comp,
	Push,
	Jmp,
	Jz,
	PushVar,
	PopVar,
	Adjsp,
	PopArgs,
	Call,
	PushCVar,
	PopCVar,
	SetTrace,
	ClrTrace,
	ClrIntDis,
	SetIntDis,
	RotateLeft,
	RotateRight,
}

pub enum Operand {
	LiteralBit32(u32),
	LiteralBit64(u64),
	LiteralString(String),
	IdRef(u32),
}

pub struct Instruction {
	pub op: Opcode,
	pub result_id: u32,
	pub result_type: u32,
	pub operands: Vec<Operand>,
}

#[derive(Default)]
pub struct Module {
	pub insts: Vec<Instruction>,
}
